<template>
    <div>
        <Header @toggleContextMenu="toggleContextMenuHandler" />
        <div>
            News
        </div>
        <Footer />
    </div>
</template>

<script>
import Header from "@/components/Header.vue"
import Footer from "@/components/Footer.vue"

